/**
* \brief Default constructor
*/
using TPos = std::tuple<float, float>;

struct Target{
    TPos content;
    bool active = false;
    std::mutex myMutex;

    void put(const TPos &data){
        std::lock_guard<std::mutex> guard(myMutex);
          content = data;
          active = true;
    }

    std::optional<TPos> get(){
        std::lock_guard<std::mutex> guard(myMutex);
        if (active)
            return content;
        else
            return {};
    }

    void set_task_finished()
    {
        std::lock_guard<std::mutex> guard(myMutex);
        active = false;
    }
};
Target target;

